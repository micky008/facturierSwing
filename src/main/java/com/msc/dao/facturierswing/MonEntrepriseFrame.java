    private void remplirMonEntreprise() {
        try {
            Request req = new Request(ENDPOINT, "moi");
            Response res = req.sendRequest();
            MonEntreprise mo = (MonEntreprise) res.getObject(MonEntreprise.class);

            jTextFieldAdresse.setText(mo.getAdresse());
            jTextFieldBic.setText(mo.getBic());
            jTextFieldCodePostal.setText(mo.getCodePostal());
            jTextFieldEmail.setText(mo.getEmail());
            jTextFieldIban.setText(mo.getIban());
            jTextFieldNom.setText(mo.getNom());
            jTextFieldPrenom.setText(mo.getPrenom());
            jTextFieldRaisonSocial.setText(mo.getRaisonSociale());
            jTextFieldSiren.setText(mo.getSiren());
            jTextFieldTelephone.setText(mo.getTelephone());
            jTextFieldTitulsaireCompte.setText(mo.getTitulairecompte());
            jTextFieldVille.setText(mo.getVille());

            res.setToken(mo.getToken());

            String ms[] = mo.getMentionLegales().split(MonEntreprise.SEPARATOR_MENTION_LEGAL);
            String row[];
            for (String ml : ms) {
                row = new String[1];
                row[0] = ml;
                ((DefaultTableModel) (jTableMentionLegale.getModel())).addRow(row);
            }
        } catch (IOException ex) {
            Logger.getLogger(MonEntrepriseFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

