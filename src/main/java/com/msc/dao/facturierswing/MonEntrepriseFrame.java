    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        MonEntreprise me = new MonEntreprise();
        me.setAdresse(jTextFieldAdresse.getText());
        me.setBic(jTextFieldBic.getText());
        me.setCodePostal(jTextFieldCodePostal.getText());
        me.setEmail(jTextFieldEmail.getText());
        me.setIban(jTextFieldIban.getText());
        me.setNom(jTextFieldNom.getText());
        me.setPrenom(jTextFieldPrenom.getText());
        me.setRaisonSociale(jTextFieldRaisonSocial.getText());
        me.setSiren(jTextFieldSiren.getText());
        me.setTelephone(jTextFieldTelephone.getText());
        me.setTitulairecompte(jTextFieldTitulsaireCompte.getText());
        me.setVille(jTextFieldVille.getText());
        List<String> mls = new ArrayList<>(jTableMentionLegale.getRowCount());
        for (int i = 0; i < jTableMentionLegale.getRowCount(); i++) {
            Object tt = jTableMentionLegale.getModel().getValueAt(i, 0);
            mls.add(tt.toString());
        }
        me.setMentionLegalesList(mls);

        Request request = new Request();
        request.setEndpoint(ENDPOINT);
        request.setUrlSuite(firstTime ? "insert" : "update");
        try {
            Response res = request.sendRequest(me, WebService.METHOD.POST);
            res.consumeToken();
            firstTime = false;
            jLabelResultat.setText("Sauvegarde OK");
        } catch (IOException | IllegalArgumentException ex) {
            Logger.getLogger(MonEntrepriseFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

